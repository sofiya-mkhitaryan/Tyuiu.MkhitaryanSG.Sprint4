        // Выводим массив
        for (int i = 0; i < rows; i++)
        {
            for (int j = 0; j < columns; j++)
            {
                Console.Write($"{mas2[i, j]} \t"); // Используем Console.Write для вывода в одной строке
            }
            Console.WriteLine();
        }

        Console.WriteLine("***************************************************************************");
        Console.WriteLine("* РЕЗУЛЬТАТ:                                                              *");
        Console.WriteLine("***************************************************************************");

        int res = ds.Calculate(mas2);
        Console.WriteLine($"Количество четных элементов матрицы: {res}");
