        Console.WriteLine("* УСЛОВИЕ:                                                                  *");
        Console.WriteLine("* Дан двумерный целочисленный массив 5 на 5 элементов,                      *");
        Console.WriteLine("* статическими значениями в диапазоне от 3 до 7.                            *");
        Console.WriteLine("* Подсчитайте количество четных элементов во всем массиве.                  *");
        Console.WriteLine("*****************************************************************************");

        int[,] mas2 = new int[5, 5] { { 4, 3, 5, 5, 3 },
                                       { 6, 7, 4, 4, 3 },
                                       { 3, 3, 7, 3, 6 },
                                       { 3, 4, 3, 7, 7 },
                                       { 3, 5, 6, 3, 6 } };

        int rows = mas2.GetLength(0); // Количество строк
        int columns = mas2.GetLength(1); // Количество столбцов

        DataService ds = new DataService();
        Console.WriteLine("*****************************************************************************");
